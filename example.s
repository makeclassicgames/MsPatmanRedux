.include "init.s"




;*****************************************************************
; Main application logic section includes the game loop
;*****************************************************************
 .segment "CODE"
 .proc main
 	; main application - rendering is currently off
	lda #TITLE
	sta current_state
	lda #0
	sta scene_loaded

	lda #176
	sta duck_y ; set duck Y position
	lda #8
	sta duck_x ; set duck X position
 	; initialize palette table
 	ldx #0
paletteloop:
	lda default_palette, x
	sta palette, x
	inx
	cpx #32
	bcc paletteloop

 	; clear 1st name table
 	jsr clear_nametable
	jsr load_scene

 	; get the screen to render
 	jsr ppu_update

 mainloop:
	jsr load_scene
	jsr draw_animation_duck
 	; skip reading controls if and change has not been drawn
 	lda nmi_ready
 	cmp #0
 	bne mainloop
	
 	; read the gamepad
 	jsr gamepad_poll
 	; now move the bat if left or right pressed
 	lda gamepad
 	and #PAD_L
 	beq NOT_GAMEPAD_LEFT
 		; gamepad has been pressed left
 		lda duck_x ; get current X
 		cmp #0
 		beq NOT_GAMEPAD_LEFT
 		sec
 		sbc #1
 		sta duck_x ; change X to the left
 NOT_GAMEPAD_LEFT:
 	lda gamepad
 	and #PAD_R
 	beq NOT_GAMEPAD_RIGHT
 		; gamepad has been pressed right
 		lda duck_x ; get current X
 		cmp #240
 		beq NOT_GAMEPAD_RIGHT
 		clc
 		adc #1
 		sta duck_x ; change X to the left
 NOT_GAMEPAD_RIGHT:
	lda gamepad
	and #PAD_START
	beq NOT_GAMEPAD_START
	    ldx current_state
		cpx #TITLE
		BNE NOT_GAMEPAD_START
		lda #0
		sta scene_loaded
		lda #PLAYING
		sta current_state
 NOT_GAMEPAD_START:
 	; ensure our changes are rendered
 	lda #1
 	sta nmi_ready
 	jmp mainloop
.endproc

.segment "CODE"
.proc clear_nametable
 	lda PPU_STATUS ; reset address latch
 	lda #$20 ; set PPU address to $2000
 	sta PPU_VRAM_ADDRESS2
 	lda #$00
 	sta PPU_VRAM_ADDRESS2

 	; empty nametable
 	lda #0
 	ldy #30 ; clear 30 rows
 	rowloop:
 		ldx #32 ; 32 columns
 		columnloop:
 			sta PPU_VRAM_IO
 			dex
 			bne columnloop
 		dey
 		bne rowloop

 	; empty attribute table
 	ldx #64 ; attribute table is 64 bytes
 	loop:
 		sta PPU_VRAM_IO
 		dex
 		bne loop
 	rts
 .endproc

.segment "CODE"
.proc load_scene
	lda scene_loaded
	cmp #1
	bne not_loaded
	rts
not_loaded:
	jsr ppu_off
	ldx current_state
	CPX #TITLE
	BNE NOT_TITLE
	; load title screen
	draw_background background_tiles_B
	JMP end_load_scene
NOT_TITLE:
	; load game screen
	draw_background background_tiles_A
end_load_scene:	
	lda #1
	sta scene_loaded
	rts
.endproc
 

;*****************************************************************
; gamepad_poll: this reads the gamepad state into the variable labelled "gamepad"
; This only reads the first gamepad, and also if DPCM samples are played they can
; conflict with gamepad reading, which may give incorrect results.
;*****************************************************************

.segment "CODE"
.proc gamepad_poll
	; strobe the gamepad to latch current button state
	lda #1
	sta JOYPAD1
	lda #0
	sta JOYPAD1
	; read 8 bytes from the interface at $4016
	ldx #8
loop:
	pha
	lda JOYPAD1
	; combine low two bits and store in carry bit
	and #%00000011
	cmp #%00000001
	pla
	; rotate carry into gamepad variable
	ror
	dex
	bne loop
	sta gamepad
	rts
.endproc

